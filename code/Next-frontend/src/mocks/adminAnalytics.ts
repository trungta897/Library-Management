function booksForRange(range: TimeRange): BorrowedBookData[] {
    return bookSeeds
        .map((book) => ({
            title: book.title,
            author: book.author,
            borrows: book.borrows[range],
            status: book.status,
            statusClass: book.statusClass,
        }))
        .sort((a, b) => b.borrows - a.borrows);
}

